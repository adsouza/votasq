/// Offline trigram-based language detection.
///
/// Uses the Cavnar & Trenkle (1994) rank-order distance algorithm with
/// 300-trigram profiles for each supported language. Works on all platforms
/// with no native dependencies.
class TrigramLanguageDetector {
  /// Returns a BCP-47 primary subtag (e.g. `"en"`, `"es"`) for [text], or
  /// `"und"` if confidence is too low.
  String detect(String text) {
    final inputProfile = _buildProfile(text);
    if (inputProfile.isEmpty) return 'und';

    var bestLang = 'und';
    var bestDistance = _maxDistance;

    for (final entry in _profiles.entries) {
      final distance = _rankDistance(inputProfile, entry.value);
      if (distance < bestDistance) {
        bestDistance = distance;
        bestLang = entry.key;
      }
    }

    // If the best distance is too high relative to the runner-up, the input
    // is likely too short or ambiguous.
    if (bestDistance > _profiles.values.first.length * 4) return 'und';

    return bestLang;
  }

  /// Returns `true` if [text] appears to be written in [language].
  ///
  /// Strategy:
  /// 1. If [detect] confidently identifies a language, compare it directly.
  /// 2. If detection is inconclusive (`und`), fall back to script analysis —
  ///    text in a different script (CJK, Cyrillic, Arabic, …) is clearly
  ///    foreign, while same-script text is assumed to be the user's language.
  bool isLanguage(String text, String language) {
    final detected = detect(text);
    if (detected != 'und') return detected == language;
    // Detection was inconclusive — check whether the text uses the same
    // script family as the target language.
    return _sameScript(text, language);
  }

  /// Returns `true` if the dominant script in [text] matches the expected
  /// script for [language].
  static bool _sameScript(String text, String language) {
    final expectedScript = _scriptForLanguage(language);
    final textScript = _dominantScript(text);
    return textScript == expectedScript;
  }

  static _Script _scriptForLanguage(String language) {
    return switch (language) {
      'ar' => _Script.arabic,
      'hi' => _Script.devanagari,
      'ja' => _Script.cjk,
      'zh' => _Script.cjk,
      'uk' => _Script.cyrillic,
      _ => _Script.latin,
    };
  }

  static _Script _dominantScript(String text) {
    var latin = 0;
    var cyrillic = 0;
    var arabic = 0;
    var devanagari = 0;
    var cjk = 0;
    for (final c in text.runes) {
      if (c >= 0x0041 && c <= 0x024F) {
        latin++;
      } else if (c >= 0x0400 && c <= 0x04FF) {
        cyrillic++;
      } else if (c >= 0x0600 && c <= 0x06FF) {
        arabic++;
      } else if (c >= 0x0900 && c <= 0x097F) {
        devanagari++;
      } else if (c >= 0x3000 && c <= 0x9FFF ||
          c >= 0xF900 && c <= 0xFAFF ||
          c >= 0xAC00 && c <= 0xD7AF) {
        cjk++;
      }
    }
    final counts = {
      _Script.latin: latin,
      _Script.cyrillic: cyrillic,
      _Script.arabic: arabic,
      _Script.devanagari: devanagari,
      _Script.cjk: cjk,
    };
    return counts.entries.reduce((a, b) => a.value >= b.value ? a : b).key;
  }

  static const _outOfPlace = 300;

  static List<String> _trigrams(String text) {
    final normalized = text
        .toLowerCase()
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();
    if (normalized.length < 3) return [];
    final result = <String>[];
    final padded = ' $normalized ';
    for (var i = 0; i < padded.length - 2; i++) {
      result.add(padded.substring(i, i + 3));
    }
    return result;
  }

  static Map<String, int> _buildProfile(String text) {
    final counts = <String, int>{};
    for (final tri in _trigrams(text)) {
      counts[tri] = (counts[tri] ?? 0) + 1;
    }
    // Rank by frequency (most frequent = rank 0).
    final sorted = counts.keys.toList()
      ..sort((a, b) => counts[b]!.compareTo(counts[a]!));
    return {for (var i = 0; i < sorted.length; i++) sorted[i]: i};
  }

  static int _rankDistance(
    Map<String, int> input,
    Map<String, int> profile,
  ) {
    var distance = 0;
    for (final entry in input.entries) {
      final profileRank = profile[entry.key];
      if (profileRank != null) {
        distance += (entry.value - profileRank).abs();
      } else {
        distance += _outOfPlace;
      }
    }
    return distance;
  }

  static const _maxDistance = 0x7FFFFFFF;

  // Top-300 trigram profiles per language, ranked by frequency.
  // Generated from representative corpora for each language.
  static final Map<String, Map<String, int>> _profiles = {
    'en': _rank(_en),
    'es': _rank(_es),
    'fr': _rank(_fr),
    'ar': _rank(_ar),
    'hi': _rank(_hi),
    'ja': _rank(_ja),
    'zh': _rank(_zh),
    'uk': _rank(_uk),
  };

  static Map<String, int> _rank(List<String> trigrams) {
    return {for (var i = 0; i < trigrams.length; i++) trigrams[i]: i};
  }

